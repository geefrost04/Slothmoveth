#!/usr/bin/env node
/**
 * migrate-math-games.mjs
 *
 * Derives 5 new game data files from the math quiz pool.
 * The math.questions.js source has `category` and `pattern` fields
 * that pre-classify each question for a specific game.
 *
 *  - analogy     : category === 'analogy'  (40)
 *  - series      : category === 'series'   (20)
 *  - matrix      : category === 'matrix'   (20)   [registered in Phase C]
 *  - logic-math  : category === 'logic'    (20)   [already in core games]
 *  - word-problem: category === 'math' AND pattern in
 *                  ['function','percentage','profit-loss','average',
 *                   'geometry','area','ratio','equation','fraction']  (~70)
 *  - speed-percent: pattern in ['percentage','profit-loss','speed-distance']  (~27)
 *
 * Output:
 *   src/courses/police_admin/data/math_analogy.ts
 *   src/courses/police_admin/data/math_series.ts
 *   src/courses/police_admin/data/math_word_problem.ts
 *   src/courses/police_admin/data/math_speed_percent.ts
 *
 * Each file is a QuizItem[] — same shape as math_quiz so the existing
 * QuizGame component can render them as fallback when the dedicated
 * component is not yet built.
 */

import fs from 'node:fs/promises';
import path from 'node:path';
import vm from 'node:vm';

const SOURCE = '/Users/geefrost/Documents/SlothMove/Page/source/police_admin/math/math.questions.js';
const TARGET = '/Users/geefrost/Desktop/SlothMove/slothmove-platform/src/courses/police_admin/data';

const WORD_PROBLEM_PATTERNS = new Set([
  'function', 'percentage', 'profit-loss', 'average',
  'geometry', 'area', 'ratio', 'equation', 'fraction'
]);
const SPEED_PERCENT_PATTERNS = new Set([
  'percentage', 'profit-loss', 'speed-distance'
]);

function extractAll(source, varNames) {
  let cleaned = source
    .replace(/\/\*[\s\S]*?\*\//g, '')
    .replace(/^\s*\/\/.*$/gm, '')
    // ES-module exports
    .replace(/^\s*export\s+default\s+\w+\s*;?\s*$/gm, '')
    .replace(/^\s*export\s*\{[^}]*\}\s*;?\s*$/gm, '')
    // `const X = ...` -> `var X = ...` so vm exposes the binding
    // on the sandbox (vm's createContext only leaks `var` bindings,
    // not `const` or `let`).
    .replace(/\bconst\s+/g, 'var ');

  const sandbox = { window: {}, self: {}, console: { log() {}, warn() {}, error() {} } };
  vm.createContext(sandbox);
  vm.runInContext(cleaned, sandbox, { timeout: 10_000 });

  const out = [];
  for (const v of varNames) {
    const data = sandbox[v] || sandbox.window[v] || sandbox.self[v];
    if (Array.isArray(data)) out.push(...data);
  }
  return out;
}

function normalizeQuestion(raw) {
  if (!raw || typeof raw !== 'object') return null;
  const question = typeof raw.question === 'string'
    ? raw.question
    : (typeof raw.q === 'string' ? raw.q : null);
  if (!question) return null;

  const choices = Array.isArray(raw.choices)
    ? raw.choices
    : (Array.isArray(raw.o) ? raw.o : null);
  if (!choices || choices.length === 0) return null;

  const answer = Number.isInteger(raw.answer)
    ? raw.answer
    : (Number.isInteger(raw.a) ? raw.a : 0);
  const explanation = typeof raw.explanation === 'string' ? raw.explanation : '';
  const hint = typeof raw.hint === 'string' ? raw.hint : '';
  const category = typeof raw.category === 'string' ? raw.category : '';
  const pattern = typeof raw.pattern === 'string' ? raw.pattern : '';

  return {
    question,
    choices: choices.map((c) => String(c ?? '')),
    answer,
    explanation,
    hint,
    category,
    pattern
  };
}

function emitTS(fileVar, items) {
  return `// AUTO-GENERATED by scripts/migrate-math-games.mjs — DO NOT EDIT
// Source: ~/Documents/SlothMove/Page/source/police_admin/math/math.questions.js (read-only)
// Derived ${items.length} items from the math quiz pool.

import type { QuizItem } from "../../../lib/course-types";

export const ${fileVar}: QuizItem[] = ${JSON.stringify(items, null, 2)};
`;
}

async function main() {
  console.log('=== migrate-math-games ===');
  const source = await fs.readFile(SOURCE, 'utf-8');
  const raw = extractAll(source, [
    'generalAbilityMathPart1', 'generalAbilityMathPart2',
    'generalAbilityMathPart3', 'generalAbilityMathPart4',
    'generalAbilityAnalogyPart1', 'generalAbilityAnalogyPart2',
    'generalAbilityMatrixPart1', 'generalAbilityLetterSeriesPart1',
    'generalAbilityLogicPart1', 'generalAbilityFractionSeriesPart1'
  ]);
  const all = raw.map(normalizeQuestion).filter(Boolean);
  console.log(`Total math questions: ${all.length}`);

  // 1. Analogy: category === 'analogy'
  const analogy = all.filter((q) => q.category === 'analogy');
  await fs.writeFile(path.join(TARGET, 'math_analogy.ts'), emitTS('math_analogy', analogy), 'utf-8');
  console.log(`analogy: ${analogy.length}`);

  // 2. Series: category === 'series'
  const series = all.filter((q) => q.category === 'series');
  await fs.writeFile(path.join(TARGET, 'math_series.ts'), emitTS('math_series', series), 'utf-8');
  console.log(`series: ${series.length}`);

  // 3. Word problem: category === 'math' AND pattern matches
  const wordProblem = all.filter((q) => q.category === 'math' && WORD_PROBLEM_PATTERNS.has(q.pattern));
  await fs.writeFile(path.join(TARGET, 'math_word_problem.ts'), emitTS('math_word_problem', wordProblem), 'utf-8');
  console.log(`word-problem: ${wordProblem.length}`);

  // 4. Speed / percent: pattern matches
  const speedPercent = all.filter((q) => SPEED_PERCENT_PATTERNS.has(q.pattern));
  await fs.writeFile(path.join(TARGET, 'math_speed_percent.ts'), emitTS('math_speed_percent', speedPercent), 'utf-8');
  console.log(`speed-percent: ${speedPercent.length}`);

  console.log('=== done ===');
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});